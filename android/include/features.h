#pragma once


/*
 * Globally enable _LARGEFILE64_SOURCE in Android builds when _GNU_SOURCE is
 * set.  Using a global build flag is difficult because some code uses
 * #define _LARGEFILE64_SOURCE and other code uses #define _LARGEFILE64_SOURCE 1
 * leading to redefinition errors when either value is used as a global flag.
 */
#if defined(_GNU_SOURCE)
#if !defined(_LARGEFILE64_SOURCE)
#define _LARGEFILE64_SOURCE
#endif
#endif


